import hashlib
import gmpy2
import math


# 合并两个同余方程的函数
def merge(a1, n1, a2, n2):
    # 计算两个模数的最大公约数
    d = math.gcd(n1, n2)
    # 计算两个余数的差值
    c = a2 - a1
    # 检查是否有解：根据贝祖定理，c必须能被d整除
    if c % d != 0:
        return 0  # 无解

    # 将c转换为正数（模n2意义下）
    c = (c % n2 + n2) % n2
    # 除以最大公约数进行简化
    c = c // d
    n1 = n1 // d
    n2 = n2 // d
    # 计算n1在模n2意义下的逆元
    c *= gmpy2.invert(n1, n2)
    # 取模得到系数k
    c %= n2
    # 计算合并后的解
    c *= n1 * d
    c += a1
    # 全局变量，存储合并后的模数和余数
    global n3
    global a3
    # 新的模数是n1*n2*d
    n3 = n1 * n2 * d
    # 新的余数，确保为正数
    a3 = (c % n3 + n3) % n3
    return 1  # 合并成功


# 扩展中国剩余定理主函数
def exCRT(a, n):
    # 初始化第一个方程
    a1 = a[0]  # 第一个余数
    n1 = n[0]  # 第一个模数
    le = len(a)  # 方程个数

    # 依次合并所有方程
    for i in range(1, le):
        a2 = a[i]  # 当前方程的余数
        n2 = n[i]  # 当前方程的模数
        # 尝试合并当前方程与已有结果
        if not merge(a1, n1, a2, n2):
            return -1  # 合并失败，无解
        # 更新为合并后的结果
        a1 = a3
        n1 = n3

    # 全局变量，存储最终的模数
    global mod
    mod = n1
    # 返回最小正整数解
    return (a1 % n1 + n1) % n1


