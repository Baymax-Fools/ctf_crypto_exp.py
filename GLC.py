def compute_p(s):
    diffs = []
    for i in range(7):  # 我们有 s0..s9，所以 i 最大到 6 保证 s[i+3] 存在
        num1 = s[i+1] * (s[i] - s[i+2]) * (s[i+1] - s[i+2])
        num2 = s[i+2] * (s[i+1] - s[i+3]) * (s[i] - s[i+1])
        diffs.append(num1 - num2)
    p = diffs[0]
    for d in diffs[1:]:
        p = gcd(p, d)
    return p

